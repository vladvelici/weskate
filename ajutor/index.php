Proiectul WeSkate a fost înființat în noiembrie 2007, când un grup de omuleți (copii) au vrut să facă un site unde să posteze poze, știri și articole despre skateboarding. Evident, atunci site-ul a fost primitiv:
<br/><br/>
<strong>WeSkate 1.0</strong> = O tematică luată de pe net + PHP-Fusion + header personalizat
<br/><br/>
OK, după accea au apărut mici modificări la versiunea a doua, dar minore. Arăta puțin mai bine, dar aceeași funcționalitate banală. Totusi, era un progres pentru vremea respectivă.<br/><br/>
La <strong>WeSkate 3</strong>, au apărut schimbări majore de design și funcționalitate. Utilizatorii puteau adăuga poza în profil, aveau URL-uri personalizate, aveam o secțiune video bine pusă la punct. Un progres foarte mare față de celălate versiuni. Se baza tot pe vechiul PHP-Fusion v6.<br />
Puteți admira un mic screenshot de pe acea vreme:<br />
<img src='http://img.weskate.ro/wsk3.jpg' /><br /><br />
<strong>Versiunea 4</strong> a venit odată cu trecerea la PHP-Fusion v7. A apărut un mic concept de <em>social</em>, aveam un sistem de prieteni, un design mai profesional aș putea spune. Îmi pare foarte rău că am reușit să pierd toate screenshot-urile. De aici a pornit ideea "My WeSkate", dar sub o altă denumire și funcționalitate, dar același concept. Pe vremea WeSkate 4 era disponibilă secțiunea "Members home", de unde membrii trimiteau submisii de știri, articole și postau în shoutbox. Tot de acolo puteau vedea video-urile puse la favorite.<br/>
Design-ul avea două culori: verde și roșu. Utilizatorii o puteau alege pe cea favorită. <em>Inițial s-au vrut 3 culori, dar albastru nu arăta prea bine, așa că am renunțat la el...</em>
<br/><br/>
<strong>Weskate 5.0 și WeSkate 5.1</strong>. Design-ul este cel actual la ambele versiuni. Marile diferențe sunt la ceea ce stă în spatele a ceea ce se vede. WeSkate 5 era bazat pe CMS-ul PHP-Fusion v7, iar de la versiunea WeSkate 5.1 nu mai folosim nici un CMS. Sunt unele diferențe de funcționalitate, cum ar fi la editarea profilului și detaliilor despre cont. În această versiunea s-au vrut adăugate validatorul de trick-uri și jocurile "guess the trick" și "horse online", dar - din păcate - au fost amânate până la următoarea versiune.


