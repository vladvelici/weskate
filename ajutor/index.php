Proiectul WeSkate și acest site (weskate.ro) sunt încă online din dorința echipei de a ajuta skaterii din toată țara cu informații utile, cum ar fi locuri de skate pe orașe, știri, interviuri, tutoriale și un forum dedicat 100% skateboarding-ului. În viitorul cât mai apropiat vom avea de oferit jocuri (<strong>HORSE online</strong> și <strong>Guess the trick</strong>) și o piață unde veți putea vinde și cumpăra componente de skate sau alte chestii pentru skateboarding.

